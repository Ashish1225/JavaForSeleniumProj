package day3;

public class DataTypesDemo2 {

	public static void main(String[] args) {
		boolean status = true;
		System.out.println(status);
		
		char c1 = 67;
		System.out.println(c1);
		
		char c2 = 'b';
		System.out.println(c2);
		
		// down casting
		byte marks = (byte)130;
		System.out.println(marks);
		
		short amount = (short)32769;
		System.out.println(amount);
		
		int number = 1923456789;
		System.out.println(number);
	
		long phoneNumber = 8045849386l;
		System.out.println(phoneNumber);
		
		float stuMarks = 56.9f;
		System.out.println(stuMarks);
		
		double teachersMarks = 20.4;
		System.out.println(teachersMarks);

	}

}
